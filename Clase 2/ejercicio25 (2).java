
//25) Utilizando un for, mostrar de 4 en 4 iniciando en 1 y hasta 500.
package Clase2;

public class ejercicio25 {

    public static void main(String[] args) {

        for (int i = 1; i <= 500; i ++) {
            if (i % 4 == 0) {
                System.out.println(i);
            }

        }

    }
}